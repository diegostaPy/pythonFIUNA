import numpy as np
# Crear un array de ejemplo
a = np.array([[1.6, 2, 3], [4, 5, 6]])
# Mostrar la forma del array
print("a.shape:", a.shape)  # Dimensiones del array
# Mostrar la longitud del array
print("len(a):", len(a))  # Longitud del array
# Mostrar el número de dimensiones del array
print("a.ndim:", a.ndim)  # Número de dimensiones del array
# Mostrar el número de elementos del array
print("a.size:", a.size)  # Número de elementos del array
# Mostrar el tipo de datos de los elementos del array
print("a.dtype:", a.dtype)  # Tipo de datos de los elementos del array
# Mostrar el nombre del tipo de datos de los elementos del array
print("a.dtype.name:", a.dtype.name)  # Nombre del tipo de datos de los elementos del array
# Convertir el array a un tipo de datos diferente
print("a.astype(int):", a.astype(int))  # Convertir el array a un tipo de datos diferente
